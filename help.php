<h2>Manually Inserting Ads Into Content</h2>
<div style='margin-left:20px'>
	To insert the ads after they have been set up at the users profile, the user should enter the following code.
	<ul>
		<li>To insert a basic ad <pre style='margin-left:20px;'>[maa id='basic']</pre></li>
		<li>To insert an advanced ad <pre style='margin-left:20px;'>[maa id='advanced']</pre> (advanced mode ads are responsive and will serve the largest ad available for the content)</li>
		<li>To insert a standard ad <pre style='margin-left:20px;'>[maa id='1']</pre> (the id here is actually the ID of the standard ad slot they want to enter, and can be something other than 1)</li>
	</ul>

</div>
